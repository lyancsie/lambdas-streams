package Exercise10;

public class Fox {
  
  private String name;
  private String color;
  private Integer age;
  
  public String getName() {
    return name;
  }
  
  public String getColor() {
    return color;
  }
  
  public Integer getAge() {
    return age;
  }
  
  public Fox(String name, String color, Integer age) {
    this.name = name;
    this.color = color;
    this.age = age;
  }
  
  public Fox() {
  }
}
